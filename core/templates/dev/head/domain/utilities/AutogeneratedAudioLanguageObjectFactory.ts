/**
 * @fileoverview Object factory for creating autogenerated audio languages.
 */

import { Injectable } from '@angular/core';
import { downgradeInjectable } from '@angular/upgrade/static';

export class AutogeneratedAudioLanguage {
  id: string;
  description: string;
  explorationLanguage: string;
  speechSynthesisCode: string;
  speechSynthesisCodeMobile: string;

  constructor(
      id: string, description: string, explorationLanguage: string,
      speechSynthesisCode: string, speechSynthesisCodeMobile: string) {
    this.id = id;
    this.description = description;
    this.explorationLanguage = explorationLanguage;
    this.speechSynthesisCode = speechSynthesisCode;
    this.speechSynthesisCodeMobile = speechSynthesisCodeMobile;
  }
}

@Injectable({
  providedIn: 'root'
})
export class AutogeneratedAudioLanguageObjectFactory {
  // TODO(#7176): Replace 'any' with the exact type. This has been kept as
  // 'any' because 'autogeneratedAudioLanguageDict' is a dict with
  // underscore_cased keys which give tslint errors against underscore_casing
  // in favor of camelCasing.
  createFromDict(
      autogeneratedAudioLanguageDict: any): AutogeneratedAudioLanguage {
    return new AutogeneratedAudioLanguage(
      autogeneratedAudioLanguageDict.id,
      autogeneratedAudioLanguageDict.description,
      autogeneratedAudioLanguageDict.exploration_language,
      autogeneratedAudioLanguageDict.speech_synthesis_code,
      autogeneratedAudioLanguageDict.speech_synthesis_code_mobile);
  }
}
angular.module('oppia').factory(
  'AutogeneratedAudioLanguageObjectFactory',
  downgradeInjectable(AutogeneratedAudioLanguageObjectFactory));
